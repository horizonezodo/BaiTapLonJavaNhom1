    private void sendbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendbtnActionPerformed
       String phanhoi = phtxt.getText();
       boolean check = true;
       if(phanhoi.equals("") != true){
            try {
            PrintWriter pw = new PrintWriter("src\\phanhoi.txt");
            pw.write(phanhoi);
            pw.write("\n");
            pw.flush();
            pw.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
            JOptionPane.showMessageDialog(rootPane, "Phản hồi của bạn đã được gửi đến khoa");
            this.phtxt.setText("");
       }
    }//GEN-LAST:event_sendbtnActionPerformed

