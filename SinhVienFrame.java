    private void timbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timbtnActionPerformed
       model.setRowCount(0);
       int count = 0;
       String msvfound = this.msvtxt.getText();
        for (SinhVien sv : Data.dsSV) {
            if(sv.getMaSV().equals(msvfound)){
                model.addRow(new Object[]{sv.getMaSV(),sv.getHoTen(),sv.getDiaChi(),sv.getEmail(),sv.getDiemTK()});
                count++;
            }
        }
        this.msvtxt.setText("");
        if(count == 0){
            JOptionPane.showMessageDialog(rootPane, "Sinh viên không tồn tại trong danh sách");
        }
    }//GEN-LAST:event_timbtnActionPerformed

